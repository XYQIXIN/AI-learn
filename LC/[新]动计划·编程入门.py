# 题目9：数字根（各位数字相加）
# ==============================================================================
# 【题目要求】
# 给定一个非负整数 num，反复将各位数字相加，直到结果为一位数
# 返回这个一位数
# 例如：38 → 3+8=11 → 1+1=2，返回 2
#
# 【知识点】
# 1. while 循环：满足条件持续循环（当 num >= 10 时循环）
# 2. 数字转字符串：str(num) 将数字转为字符串
# 3. 字符串遍历：for ch in s 遍历每个字符
# 4. 字符转数字：int(ch) 将字符转回数字
# 5. 数字各位相加：累加器模式
#
# 【数字每一位操作的两种方式】
# 本题用的是：转字符串遍历
#   s = str(num)
#   for ch in s:
#       total += int(ch)
#
# 另一种方式（数学取模）：
#   while num > 0:
#       digit = num % 10    # 获取最后一位
#       total += digit
#       num = num // 10      # 去掉最后一位
#
# 【解决什么问题】
# 理解 while 循环的使用场景（不知道要循环多少次）
# 掌握数字转字符串后逐位处理的方法
# 数字根在数论中有重要应用（数根性质）
# ==============================================================================
class Solution(object):
    def addDigits(self, num):
        while num >= 10:      # 当 num 是两位数或多位数时循环
            s = str(num)      # 将数字转为字符串
            total = 0
            for ch in s:      # 遍历字符串中的每个字符（每一位数字）
                total += int(ch)  # 将字符转数字并累加
                num = total   # 更新 num 为各位之和
        return num



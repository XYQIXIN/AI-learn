# 题目18：山脉数组的峰顶索引
# ==============================================================================
# 【题目要求】
# 给定一个山脉数组 arr，满足：
# - arr.length >= 3
# - 存在 i（0 < i < arr.length-1）使得 arr[0] < arr[1] < ... < arr[i] > arr[i+1] > ... > arr[arr.length-1]
# 求峰顶索引 i
#
# 【知识点】
# 1. 二分查找：在有序/半有序数组中快速查找
# 2. 左闭右开：while left < right（left 和 right 是索引范围）
# 3. 取整除法：// 2（向下取整）
# 4. 夹逼法：逐渐缩小搜索范围
#
# 【二分查找核心逻辑】
# - arr[mid] < arr[mid + 1]：说明峰顶在右边，left = mid + 1
# - 否则：峰顶在左边或就是 mid，right = mid
# - 循环结束时 left == right，即为峰顶
#
# 【为什么用二分查找】
# 山脉数组先升后降，具有二段性
# 可以用 O(log n) 时间复杂度找到峰顶
#
# 【解决什么问题】
# 掌握二分查找模板
# 理解夹逼缩小范围的思想
# 学会处理边界情况
# ==============================================================================
class Solution:
    def peakIndexInMountainArray(self, arr: List[int]) -> int:
        left = 0
        right = len(arr) - 1
        while left < right:
            mid = (left + right) // 2
            # 如果中间值小于右边，说明在上升段，峰顶在右边
            if arr[mid] < arr[mid + 1]:
                left = mid + 1
            else:
                # 否则在下降段或就是峰顶
                right = mid
        return left
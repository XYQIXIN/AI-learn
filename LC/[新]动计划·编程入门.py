# 题目2：温度转换
# ==============================================================================
# 【题目要求】
# 将给定摄氏度转换为开尔文温度和华氏温度
# - 开尔文 = 摄氏度 + 273.15
# - 华氏度 = 摄氏度 × 1.80 + 32.00
#
# 【知识点】
# 1. 浮点数运算（float）
# 2. 数学公式应用
# 3. 列表返回值（List[float]）
# 4. 类型注解（Type Hints）

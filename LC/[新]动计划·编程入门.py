# 题目4：二叉树 - 验证二叉树
# ==============================================================================
# 【题目要求】
# 给定一个二叉树的根节点 root，验证根节点的值是否等于左右子节点值之和
#
# 【知识点】
# 1. 二叉树基础结构：每个节点有 val（值）、left（左孩子）、right（右孩子）
# 2. 树节点访问：通过 root.val、root.left.val、root.right.val 获取节点值
# 3. 布尔返回值：返回 True 或 False
# 4. Optional 类型：表示可能为 None 的类型（Python 中用 Optional[TreeNode] 表示）
#
# 【解决什么问题】
# 学习二叉树的基本概念和节点访问方式
# 注意：实际解题时应考虑空指针情况（root.left 或 root.right 可能为 None）
# ==============================================================================
# Definition for a binary tree node.
# class TreeNode(object):
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution(object):
    def checkTree(self, root):
        if root.val == root.left.val + root.right.val:
            return True
        else:
            return False



# 题目13：丑数
# ==============================================================================
# 【题目要求】
# 给定一个整数 n，判断它是否是丑数
# 丑数：只包含质因数 2, 3, 5 的正整数
# 例如：1, 2, 3, 4, 5, 6, 8, 9, 10 是丑数
#      14 = 2*7（有质因数7）不是丑数
#
# 【知识点】
# 1. 遍历列表：for factor in [2,3,5]
# 2. 质因数分解：反复除以相同因数直到除不尽
# 3. return n == 1：如果最后剩下 1，说明只有 2,3,5 质因数
# 4. 列表推导式简写
#
# 【解题思路】
# 反复除以 2, 3, 5，能除尽就继续除
# 如果最后结果是 1，说明只包含这些质因数
#
# 【解决什么问题】
# 学会遍历多个值：for factor in [2,3,5]
# 理解 return n == 1 的含义（最终判断）
# ==============================================================================
class Solution(object):
    def isUgly(self, n):
        if n <= 0:
            return False
        for factor in [2, 3, 5]:
            while n % factor == 0:
                n //= factor
        return n == 1



# 题目11：2的幂
# ==============================================================================
# 【题目要求】
# 给定一个整数 n，判断它是否是 2 的幂
# 2的幂：1, 2, 4, 8, 16, 32, 64...
#
# 【知识点】
# 1. 赋值(=) vs 相等(==)：
#    - = 是赋值：将右侧的值赋给左侧变量
#    - == 是比较：判断左右两侧是否相等
# 2. 位运算：n & (n-1) 可以消除 n 最右边的 1
# 3. 逻辑运算符：and/or/not
# 4. 短路求值：and 前面为 False 时不执行后面
#
# 【位运算核心性质】
# n & (n-1) 的作用：消除 n 最右边的 1
# 例如：n = 8 (1000) → n-1 = 7 (0111) → n & (n-1) = 0
# 如果 n 是 2 的幂，则 n & (n-1) 一定等于 0
#
# 【解决什么问题】
# 理解赋值和比较的区别
# 掌握位运算技巧：判断一个数是否是 2 的幂
# ==============================================================================
class Solution(object):
    def isPowerOfTwo(self, n):
        return n > 0 and (n & (n - 1)) == 0



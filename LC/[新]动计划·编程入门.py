# 题目7：统计好三元组
# ==============================================================================
# 【题目要求】
# 给你一个整数数组 arr，以及三个整数 a、b、c
# 找出所有满足以下条件的三元组 (i, j, k)：
# - 0 <= i < j < k < len(arr)
# - |arr[i] - arr[j]| <= a
# - |arr[j] - arr[k]| <= b
# - |arr[i] - arr[k]| <= c
# 返回满足条件的三元组数量
#
# 【知识点】
# 1. 三重循环遍历（暴力枚举）
# 2. 绝对值函数 abs()：计算两个数的差的绝对值
# 3. 多条件同时满足（and 逻辑运算符）
# 4. 索引遍历：range(i+1, n) 确保 j > i，range(j+1, n) 确保 k > j
#
# 【解决什么问题】
# 学习多重循环嵌套遍历数组的暴力解法，理解索引的边界控制
# 重要：三层循环时间复杂度 O(n³)，数据量大时需优化
# ==============================================================================
class Solution(object):
    def countGoodTriplets(self, arr, a, b, c):
        n = len(arr)
        count = 0
        # 三重循环遍历所有可能的三元组
        for i in range(0, n - 2):       # i 从 0 到 n-3
            for j in range(i + 1, n):    # j 从 i+1 到 n-1
                for k in range(j + 1, n):  # k 从 j+1 到 n-1
                    diff_ij = abs(arr[i] - arr[j])  # arr[i] 和 arr[j] 的差的绝对值
                    diff_jk = abs(arr[j] - arr[k])  # arr[j] 和 arr[k] 的差的绝对值
                    diff_ik = abs(arr[i] - arr[k])  # arr[i] 和 arr[k] 的差的绝对值
                    # 三个条件同时满足
                    if diff_ij <= a and diff_jk <= b and diff_ik <= c:
                        count += 1
        return count



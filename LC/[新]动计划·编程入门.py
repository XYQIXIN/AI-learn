# 题目8：字符串大小写转换
# ==============================================================================
# 【题目要求】
# 将字符串中的大写字母转换为小写字母，其他字符保持不变
#
# 【知识点】
# 1. 字符串遍历：for ch in s
# 2. 字符判断方法：
#    - str.isupper()：判断是否为大写字母
#    - str.islower()：判断是否为小写字母
#    - str.isdigit()：判断是否为数字
# 3. 字符转换方法：
#    - str.lower()：转换为小写
#    - str.upper()：转换为大写
# 4. 字符串拼接：使用 + 运算符或 join() 方法
# 5. Python 内置方法：str.lower() 直接把整个字符串转小写（更简洁）
#
# 【解决什么问题】
# 学习字符串的遍历和字符判断，这是处理字符串的基础
# 重要：Python 有更简洁的 s.lower() 方法，但手动实现有助于理解原理
# ==============================================================================
class Solution(object):
    def toLowerCase(self, s):
        result = ""
        for ch in s:           # 遍历字符串中每个字符
            if ch.isupper():   # 判断是否为大写字母
                result += ch.lower()  # 转换为小写并拼接
            else:
                result += ch   # 非大写字母保持不变
        return result
        # 简便写法：return s.lower()



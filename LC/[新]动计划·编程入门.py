# 题目15：矩阵转置
# ==============================================================================
# 【题目要求】
# 给定一个矩阵 matrix，返回它的转置矩阵
# 转置：行变列，列变行
# 例如：[[1,2,3],[4,5,6]] → [[1,4],[2,5],[3,6]]
#
# 【知识点】
# 1. 二维列表创建：result = [[0]*rows for _ in range(cols)]
#    - 外层循环 cols 次（创建列）
#    - 内层循环 rows 次（每列 rows 个元素）
# 2. 空矩阵判断：if not matrix or not matrix[0]
#    - not matrix：矩阵为空
#    - not matrix[0]：矩阵第一行为空
# 3. len()：获取列表长度
# 4. 二维数组遍历：for i in range(rows): for j in range(cols)
# 5. 矩阵转置核心：result[j][i] = matrix[i][j]
#
# 【重点解释：result = [[0]*rows for _ in range(cols)]】
# 假设 rows=2, cols=3：
# - 外层 range(cols) 执行 3 次 → 创建 3 行
# - 内层 [0]*rows 创建 [0, 0]（2 个元素）
# - 结果：[[0,0], [0,0], [0,0]]（3行2列）
#
# 【重点解释：if not matrix or not matrix[0]】
# - matrix = []：空矩阵，not matrix = True
# - matrix = [[]]：有一个空行，not matrix[0] = True
# 两种情况都返回空列表
#
# 【解决什么问题】
# 掌握二维列表的创建方法
# 理解列表推导式的嵌套
# 学会处理空矩阵边界情况
# ==============================================================================
class Solution(object):
    def transpose(self, matrix):
        if not matrix or not matrix[0]:  # 空矩阵检查
            return []
        rows = len(matrix)       # 原矩阵行数
        cols = len(matrix[0])   # 原矩阵列数
        # 创建转置后的矩阵（cols 行，rows 列）
        result = [[0] * rows for _ in range(cols)]
        # 遍历原矩阵，转置赋值
        for i in range(rows):
            for j in range(cols):
                result[j][i] = matrix[i][j]
        return result

# 题目12：3的幂
# ==============================================================================
# 【题目要求】
# 给定一个整数 n，判断它是否是 3 的幂
# 3的幂：1, 3, 9, 27, 81, 243...
#
# 【知识点】
# 1. 整除运算符：//=（a //= b 等价于 a = a // b）
# 2. 取模运算符：%（a % b 返回余数）
# 3. 逻辑判断：if/else
# 4. 缩进：Python 用缩进表示代码块（同一缩进级别=同一块代码）
# 5. while 循环：条件为 True 时重复执行
#
# 【解题思路】
# 反复除以 3，除得尽说明是 3 的幂，最终结果为 1
# 例如：81 → 27 → 9 → 3 → 1（是）
#      10 → 3 → 1（不是，余数不为0）
#
# 【解决什么问题】
# 掌握整除运算符 //= 的用法
# 理解缩进在 Python 中的重要性
# 学会用 while 循环分解问题
# ==============================================================================
class Solution(object):
    def isPowerOfThree(self, n):
        if n <= 0:
            return False
        while n % 3 == 0:
            n //= 3
        return n == 1



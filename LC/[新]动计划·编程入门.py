# 【Python 基础语法总结】for / if / while 的区别和用法
# ==============================================================================
# 【for 循环】
# - 用于已知循环次数的遍历
# - 语法：for 变量 in 序列（列表、字符串、range等）
# - 场景：遍历数组、字符串、字典；对数据进行迭代处理
# - 示例：
#   for i in range(5):      # 循环5次，i 从 0 到 4
#   for item in list:       # 遍历列表每个元素
#   for ch in string:       # 遍历字符串每个字符
#
# 【while 循环】
# - 用于未知循环次数，满足条件持续循环
# - 语法：while 条件表达式:
# - 场景：不知道要循环多少次，需要根据条件终止
# - 示例：
#   while num >= 10:        # 当 num >= 10 时持续循环
#   while True:             # 无限循环（需配合 break 退出）
#
# 【if 条件判断】
# - 用于根据条件选择执行不同代码
# - 语法：if 条件1: ... elif 条件2: ... else: ...
# - 场景：判断真假、分支处理
# - 示例：
#   if x > 0:              # 判断正数
#   if x % 2 == 0:         # 判断偶数
#
# 【三者对比】
# - for：确定次数的遍历 → "我要遍历n次"
# - while：条件驱动的循环 → "满足条件就继续"
# - if：一次性判断 → "判断一次，执行一次"
#
# 【数字每一位操作的两种方式】
# 方式1：转字符串遍历（适合各位独立操作）
#   s = str(n)
#   for ch in s:
#       digit = int(ch)    # 每一位数字
#
# 方式2：数学取模（适合数值计算）
#   while n > 0:
#       digit = n % 10     # 获取最后一位
#       n = n // 10        # 去掉最后一位

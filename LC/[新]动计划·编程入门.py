# ==============================================================================
# 题目2：温度转换
# ==============================================================================
# 【题目要求】
# 将给定摄氏度转换为开尔文温度和华氏温度
# - 开尔文 = 摄氏度 + 273.15
# - 华氏度 = 摄氏度 × 1.80 + 32.00
#
# 【知识点】
# 1. 浮点数运算（float）
# 2. 数学公式应用
# 3. 列表返回值（List[float]）
# 4. 类型注解（Type Hints）
#
# 【解决什么问题】
# 简单的数学公式应用题，学习如何处理浮点数和返回列表
# ==============================================================================
class Solution(object):
    def convertTemperature(self, celsius):
        kelvin = float(celsius + 273.15)       # 摄氏度转开尔文：K = C + 273.15
        fahrenheit = float(celsius * 1.80 + 32.00)  # 摄氏度转华氏度：F = C × 1.8 + 32
        return [kelvin, fahrenheit]


# ==============================================================================
# 题目3：求最小偶数倍数
# ==============================================================================
# 【题目要求】
# 给定一个正整数 n，返回 n 的最小偶数倍数
# 如果 n 本身就是偶数，返回 n；否则返回 n*2
#
# 【知识点】
# 1. 取模运算符 (%)：用于判断奇偶性
# 2. 条件判断 (if-else)
# 3. 偶数判断：n % 2 == 0 表示偶数，n % 2 == 1 表示奇数
#
# 【解决什么问题】
# 学习使用取模运算判断数字的奇偶性，这是算法中的基础操作
# 拓展：任意两个数的最小公倍数 lcm(a, b) = a * b / gcd(a, b)
#       这里因为 2 是素数，所以最小公倍数就是 n 或 n*2
# ==============================================================================
class Solution(object):
    def smallestEvenMultiple(self, n):
        if n % 2 == 0:      # 判断 n 是否为偶数（能被2整除）
            return n         # n 是偶数，直接返回 n
        else:
            return n * 2     # n 是奇数，返回 n*2（最小偶数倍数）



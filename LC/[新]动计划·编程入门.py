# 题目5：异或操作
# ==============================================================================
# 【题目要求】
# 给你两个整数 n 和 start，生成一个数组：
# - 数组长度为 n
# - 第 i 个元素 = start + 2*i（i 从 0 到 n-1）
# - 返回数组所有元素的异或结果
#
# 【知识点】
# 1. 异或运算符 (^)：
#    - 相同为 0，不同为 1：0^0=0, 1^1=0, 0^1=1, 1^0=1
#    - 任何数与 0 异或等于本身：x^0 = x
#    - 任何数与自身异或等于 0：x^x = 0
#    - 异或满足交换律和结合律：a^b^c = a^c^b
# 2. 循环遍历 (for range)
# 3. 累加异或（用 ^= 代替累加）
#
# 【解决什么问题】
# 理解异或运算的性质，这是位运算中的核心考点
# 异或技巧：
#   - 查找数组中唯一出现一次的数字（其他出现两次）
#   - 不使用临时变量交换两个数：a = a^b, b = a^b, a = a^b
# ==============================================================================
class Solution(object):
    def xorOperation(self, n, start):
        result = 0
        for i in range(n):
            # 生成数组元素：start + 2*i（等差数列，公差为2）
            # 累加异或：result = result ^ (start + 2*i)
            result ^= start + 2 * i
        return result



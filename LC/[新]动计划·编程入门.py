# 题目6：统计相同元素对
# ==============================================================================
# 【题目要求】
# 给定一个整数数组 nums，统计满足以下条件的好元素对 (i, j) 的数量：
# - i < j
# - nums[i] == nums[j]
# 即统计数组中相同元素可以组成的索引对数量
# 例如：[1,2,3,1,1,3] 中，(0,3)、(0,4)、(1,5)、(3,4)、(4,5) 共5对
#
# 【知识点】
# 1. 双重循环遍历数组（暴力枚举）
# 2. 嵌套循环：外层 i，内层 j 从 i+1 开始（避免重复和自身比较）
# 3. range() 函数：range(0, len(nums)) 或 range(len(nums))
# 4. 条件判断：比较两个元素是否相等
# 5. 计数器累加：count += 1 或 count = count + 1
#
# 【暴力解法 vs 最优解】
# 暴力解：双重循环，时间复杂度 O(n²)
# 最优解：使用哈希表/字典统计每个元素出现次数，公式：n*(n-1)/2
# 示例：元素 1 出现 3 次，则有 3*2/2 = 3 对
#
# 【解决什么问题】
# 学习数组遍历和双重循环的使用
# 理解索引关系 i < j 的实现方式（j 从 i+1 开始）
# 初步认识哈希表优化（后续会学到）
# ==============================================================================
class Solution(object):
    def numIdenticalPairs(self, nums):
        count = 0
        # 双重循环遍历所有可能的 (i, j) 对
        for i in range(len(nums)):        # i 从 0 到 n-1
            for j in range(i + 1, len(nums)):  # j 从 i+1 到 n-1，确保 i < j
                if nums[i] == nums[j]:     # 判断两个元素是否相等
                    count += 1             # 相等则计数 +1
        return count



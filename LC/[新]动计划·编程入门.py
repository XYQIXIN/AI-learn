# 题目10：数字的各位之积与各位之和差
# ==============================================================================
# 【题目要求】
# 给定一个整数 n，求其各位数字的乘积与各位数字的和之差
# 例如：n = 234 → 乘积 = 2*3*4=24，和 = 2+3+4=9，返回 24-9=15
#
# 【知识点】
# 1. 数字转字符串：str(n) 将整数转为字符串
# 2. 字符串遍历：for ch in s 遍历每个字符
# 3. 字符转数字：int(ch) 将字符转回数字
# 4. 累乘器：初始值为 1，不断乘以每一位
# 5. 累加器：初始值为 0，不断加上每一位
# 6. 两者结合：同时计算乘积与和
#
# 【解决什么问题】
# 这是你提到的"对数字每一位操作"的典型题目！
# 掌握转字符串后用 for 遍历每一位的方法
# 理解累加器和累加器的区别和应用确定初始值
# ==============================================================================
class Solution(object):
    def subtractProductAndSum(self, n):
        s = str(n)           # 将整数转为字符串
        product = 1          # 累乘器：初始值必须为 1（乘法单位元）
        sum_digits = 0       # 累加器：初始值为 0（加法单位元）
        for ch in s:         # 遍历字符串中的每个字符
            digit = int(ch)  # 将字符转为数字
            product *= digit  # 累乘：product = product * digit
            sum_digits += digit  # 累加：sum_digits = sum_digits + digit
        return product - sum_digits  # 返回乘积与和的差


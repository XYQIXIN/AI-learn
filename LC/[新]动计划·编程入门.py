# 题目17：元音字符串
# ==============================================================================
# 【题目要求】
# 给定一个字符串数组 words 和范围 [left, right]
# 找出索引在 [left, right] 范围内的"元音字符串"数量
# 元音字符串：首字母和尾字母都是元音（a, e, i, o, u）的字符串
#
# 【知识点】
# 1. 集合：vowels = {'a', 'e', 'i', 'o', 'u'}
#    - 集合用 {}，列表用 []
#    - 集合查找效率高：O(1)
# 2. 负索引：word[-1] 表示最后一个字符
#           word[-2] 表示倒数第二个
# 3. in 操作符：判断元素是否在集合/列表中
# 4. 范围遍历：range(left, right + 1)
#
# 【为什么用集合而不是列表】
# 集合查找比列表快（哈希表实现）
# 用 in vowels 判断是否是元音
#
# 【解决什么问题】
# 学会用集合存储多个值
# 掌握负索引的使用
# 理解范围遍历的边界
# ==============================================================================
class Solution:
    def vowelStrings(self, words: List[str], left: int, right: int) -> int:
        vowels = {'a', 'e', 'i', 'o', 'u'}  # 集合：元音字母
        count = 0
        for i in range(left, right + 1):
            word = words[i]
            # 首尾都是元音才是元音字符串
            if word[0] in vowels and word[-1] in vowels:
                count += 1
        return count



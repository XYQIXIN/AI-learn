# 题目16：分割字符串的最大得分
# ==============================================================================
# 【题目要求】
# 给定一个字符串 s（只包含 '0' 和 '1'），在索引 i 处分割成左右两部分
# 左边得分：左边字符串中 '0' 的个数
# 右边得分：右边字符串中 '1' 的个数
# 求最大得分（注意：必须至少分割一次，即左边非空，右边非空）
#
# 【知识点】
# 1. 字符串.count()：统计字符串中某个字符出现的次数
# 2. 字符串切片：s[:i] 表示从开头到索引 i-1
# 3. 字符串索引：s[i] 访问单个字符
# 4. 累加器：left_zeros、left_ones 记录左边的 0 和 1
# 5. 动态计算右边：total_ones - left_ones
#
# 【解题思路】
# 遍历每个可能的分割点，计算当前得分
# 左边用累加器，右边用总数减去左边
# s.count('1')：快速统计整个字符串中 '1' 的个数
#
# 【解决什么问题】
# 掌握 .count() 方法统计字符个数
# 学会用累加器动态计算
# 理解分割问题的左右得分计算
# ==============================================================================
class Solution(object):
    def maxScore(self, s):
        n = len(s)
        total_ones = s.count('1')  # 统计字符串中 '1' 的总个数
        left_zeros = 0              # 左边 '0' 的个数
        left_ones = 0               # 左边 '1' 的个数
        max_score = 0              # 最大得分
        # 遍历到 n-1 为止（保证右边非空）
        for i in range(n - 1):
            if s[i] == '0':
                left_zeros += 1    # 左边 '0' 加 1
            else:
                left_ones += 1     # 左边 '1' 加 1
            # 得分 = 左边的 '0' + 右边的 '1'
            score = left_zeros + (total_ones - left_ones)
            if score > max_score:
                max_score = score
        return max_score



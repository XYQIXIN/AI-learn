# 题目14：数组洗牌
# ==============================================================================
# 【题目要求】
# 给定一个数组 nums（前2n个元素），按 [x0, x1, ..., x(n-1), y0, y1, ..., y(n-1)] 顺序重排
# 例如：nums = [2,5,1,3,4,7], n = 3
#      结果：[2,3,4,5,7,1]
#      解释：前3个 [2,5,1]，后3个 [3,4,7]
#            重新组合：[2,3,4,5,7,1]（交替取）
#
# 【知识点】
# 1. 列表创建：res = []
# 2. range(n)：生成 0 到 n-1 的序列
# 3. append()：在列表末尾添加元素
# 4. 索引访问：nums[i]、nums[n+i]
# 5. 交替合并：先取前 n 个，再取后 n 个
#
# 【解决什么问题】
# 掌握 for i in range(n) 的用法
# 学会使用 append() 添加元素
# 理解数组索引的灵活运用
# ==============================================================================
class Solution(object):
    def shuffle(self, nums, n):
        res = []
        for i in range(n):
            res.append(nums[i])      # 先取前 n 个元素
            res.append(nums[n + i])  # 再取后 n 个元素
        return res


